
var nome = "Ruan"
console.log(typeof(nome))//retorna o tipo string

var idade = 22
console.log(typeof(idade))//retorna um tipo number (numero)

var solteiro = true
console.log(typeof(solteiro))//retorna um tipo boolean

var nada;
console.log(typeof(nada))//retorna um tipo undefined (não definida)

var sym = Symbol("teste")
console.log(typeof(sym))//retorna um tipo simbol    

var pessoa = {Nome:'Ruan', faculdade:'Iespes'}
console.log(typeof(pessoa))//retorna um tipo objeto (dicionário)

var gosto_frutas = ['Abacate', 'Mamão', 'Manga']
