
var nome = "Ruan"
var sobrenome = "Carlos"

console.log(nome.charAt(0))//pega a letra que está no indice do parâmetro

console.log(nome.concat(sobrenome))//concatena uma variável com outra

console.log(nome.indexOf('n'))//pega o indice (posição) de uma determinada letra

console.log(nome.replace("Ru", "ur")) //substitui um trecho da variável por outro

console.log(sobrenome.split('r')) //apaga a letra que está no parâmetro e divide as extremidades.

console.log(nome.toUpperCase())//converte tudo em letras maiusculas

console.log(nome[2])//pega a letra correspondente ao indice que que está no parâmetro

console.log(nome.repeat(5))//repete a variavel o tanto de vezes que são passados nos parâmetros

console.log(nome.search('n'))//procura o indice de uma determinada variável e retorna esse indice




